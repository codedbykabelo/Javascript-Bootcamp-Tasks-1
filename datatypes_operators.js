// Numbers

let integerNumber = 10;
let floatNumber = 5.5;

let addition = integerNumber + 2;
let subtraction = integerNumber - 2;
let multiplication = integerNumber * 2;
let division = integerNumber / 2;
let modulus = integerNumber % 3;
let exponentiation = integerNumber ** 2;

console.log("Integer:", integerNumber);
console.log("Float:", floatNumber);

console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);
console.log("Modulus:", modulus);
console.log("Exponentiation:", exponentiation);


// Boolean and Operators

// Comparison Operators

let num1 = 15;
let num2 = 10;

let greaterThan = num1 > num2;
let lessThan = num1 < num2;
let equalTo = num1 === num2;
let notEqualTo = num1 !== num2;

console.log("Greater Than:", greaterThan);
console.log("Less Than:", lessThan);
console.log("Equal To:", equalTo);
console.log("Not Equal To:", notEqualTo);


// x and y comparison

let x = 8;
let y = 12;

console.log("x > y:", x > y);
console.log("x <= y:", x <= y);
console.log("x == y:", x == y);
console.log("x != y:", x != y);


// Logical Operators

let a = true;
let b = false;

console.log("a AND b:", a && b);
console.log("a OR b:", a || b);
console.log("NOT a:", !a);


